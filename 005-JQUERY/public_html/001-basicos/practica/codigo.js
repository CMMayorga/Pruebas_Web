
function cambiar(animal){
    if(animal=="perro"){
        $("img").attr("src","img/perro.jpg")
    }
    else if(animal=="gato"){
        $("img").attr("src","img/gato.jpg")
    }else if(animal=="loro"){
        $("img").attr("src","img/loro.jpg")
    }else if(animal=="pato"){
        $("img").attr("src","img/pato.jpg")
    }
}